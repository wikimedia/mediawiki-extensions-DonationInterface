		$country = substr( $country, 0, 2 );
		$country = strtoupper( $country );
		$nationalCurrencies = NationalCurrencies::getNationalCurrencies();
		if ( isset( $nationalCurrencies[$country] ) ) {
			return $nationalCurrencies[$country];
		}
		else
			return NULL;
	}

